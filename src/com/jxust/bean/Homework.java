package com.jxust.bean;

import java.util.HashSet;
import java.util.Set;

public class Homework {

	private Long id;
	private String studentClass;
	private String studentName;
	private String teacherName;
	private String topic;
	private String description;
	private String search;
	public String getSearch() {
		return search;
	}
	public void setSearch(String search) {
		this.search = search;
	}
	private Set<AttachFile> homeworkAttachFile = new HashSet<AttachFile>();
	public Set<AttachFile> getHomeworkAttachFile() {
		return homeworkAttachFile;
	}
	public void setHomeworkAttachFile(Set<AttachFile> homeworkAttachFile) {
		this.homeworkAttachFile = homeworkAttachFile;
	}
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getStudentClass() {
		return studentClass;
	}
	public void setStudentClass(String studentClass) {
		this.studentClass = studentClass;
	}
	public String getStudentName() {
		return studentName;
	}
	public void setStudentName(String studentName) {
		this.studentName = studentName;
	}
	public String getTeacherName() {
		return teacherName;
	}
	public void setTeacherName(String teacherName) {
		this.teacherName = teacherName;
	}
	public String getTopic() {
		return topic;
	}
	public void setTopic(String topic) {
		this.topic = topic;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
}
