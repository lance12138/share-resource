package com.jxust.bean;

import java.util.Date;

public class Activity {

	private Long ac_id;
	private String ac_name;
	private String ac_content;
	private Date ac_time;
	
	public Long getAc_id() {
		return ac_id;
	}
	public void setAc_id(Long acId) {
		ac_id = acId;
	}
	public String getAc_name() {
		return ac_name;
	}
	public void setAc_name(String acName) {
		ac_name = acName;
	}
	public String getAc_content() {
		return ac_content;
	}
	public void setAc_content(String acContent) {
		ac_content = acContent;
	}
	
	public Date getAc_time() {
		return ac_time;
	}
	public void setAc_time(Date acTime) {
		ac_time = acTime;
	}
	public String getAc_author() {
		return ac_author;
	}
	public void setAc_author(String acAuthor) {
		ac_author = acAuthor;
	}
	private String ac_author;
}
