package com.jxust.bean;

public class ErrorAnalyse {

	private Long error_id;
	private String error_name;
	private String error_analyse;
	private String error_photo;
	private String thumbnail;
	public String getThumbnail() {
		return thumbnail;
	}
	public void setThumbnail(String thumbnail) {
		this.thumbnail = thumbnail;
	}
	private Long courseId;
	public Long getError_id() {
		return error_id;
	}
	public void setError_id(Long errorId) {
		error_id = errorId;
	}
	public String getError_name() {
		return error_name;
	}
	public void setError_name(String errorName) {
		error_name = errorName;
	}
	public String getError_analyse() {
		return error_analyse;
	}
	public void setError_analyse(String errorAnalyse) {
		error_analyse = errorAnalyse;
	}
	public String getError_photo() {
		return error_photo;
	}
	public void setError_photo(String errorPhoto) {
		error_photo = errorPhoto;
	}
	public Long getCourseId() {
		return courseId;
	}
	public void setCourseId(Long courseId) {
		this.courseId = courseId;
	}
}
